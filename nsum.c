#include<stdio.h>
void main()
{
    int n,i,sum=0;
    printf("enter the number of elements");
    scanf("%d",&n);
    int arr[n];
    printf("enter %d elements",n);
    for(i=0;i<0;i++)
    {
        scanf("%d",&arr[i]);
        sum+=arr[i];

    }
    printf("sum of all elements=%d",sum);
}